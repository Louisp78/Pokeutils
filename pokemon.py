class Pokemon:
    def __init__(self, type, niv, pv, defence, attack, speed, defence_spe, attack_spe, talent):
        self.type = type
        self.niv = niv
        self.pv = pv
        self.defence = defence
        self.attack = attack
        self.speed = speed
        self.defence_spe = defence_spe
        self.attack_spe = attack_spe
        self.talent = talent